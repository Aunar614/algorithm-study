package jungol.stepping.function;

public class Jungol_584 {

    public static void main(String[] args) {
        define();
    }

    private static void define() {
        for (int i = 1; i <= 3; i++) {
            for (int j = 1; j <= 3; j++) {
                System.out.println(i + " + " + j + " = " + (i + j));
            }
        }
    }
}
